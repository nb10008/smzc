const INT X_DATATIME_LEN = 19;
//------------------------------------------------------------------------------------
// DWORD表示的时间,年月日小时分秒,如果把它直接转为DWORD，可以直接比较大小,但差值无意义
// 值较大者其时间也较后
//------------------------------------------------------------------------------------
struct tagDWORDTime
{
	DWORD	sec		: 6,	// 0 - 59
			min		: 6,	// 0 - 59
			hour    : 5,	// 0 - 23
			day		: 5,	// 1 - 31
			month	: 4,	// 1 - 12
			year	: 6;	// 两位数，比如:05, 表示2005.最大表示到2063年

	VOID Clear()
	{
		sec=min=hour=day=month=year=0;
	}

	//--------------------------------------------------------------------------------
	// operator =
	//--------------------------------------------------------------------------------
	tagDWORDTime& operator = (const tagDWORDTime& time)
	{ 
		sec=time.sec;
		min=time.min; 
		hour=time.hour;
		day=time.day;
		month=time.month;
		year=time.year;
		return *this;
	}

	//--------------------------------------------------------------------------------
	// operator =
	//--------------------------------------------------------------------------------
	tagDWORDTime& operator = (DWORD dwTime)
	{
		return (*this = *(tagDWORDTime*)&dwTime);
	}

	//--------------------------------------------------------------------------------
	// convertor
	//--------------------------------------------------------------------------------
	operator DWORD()
	{
		return *(DWORD*)this;
	}

	//--------------------------------------------------------------------------------
	// constructor
	//--------------------------------------------------------------------------------
	tagDWORDTime()
	{
	};

	//--------------------------------------------------------------------------------
	// copy constructor
	//--------------------------------------------------------------------------------
	tagDWORDTime(BYTE bySec,BYTE byMin,BYTE byHour,BYTE byDay,BYTE byMonth,BYTE byYear)
	{
		sec = bySec;
		min = byMin;
		hour = byHour;
		day = byDay;
		month = byMonth;
		year = byYear;
	}

	//--------------------------------------------------------------------------------
	// copy constructor
	//--------------------------------------------------------------------------------
	tagDWORDTime(const tagDWORDTime& time)
	{
		*this = time;
	}

	//--------------------------------------------------------------------------------
	// copy constructor
	//--------------------------------------------------------------------------------
	tagDWORDTime(DWORD/*&*/ dwTime)
	{
		*this = dwTime;
	}
};

//-------------------------------------------------------------------------------------
